def fetch_acled_data(api_key, email, region="South Asia", start_date=None, end_date=None, limit=1000):
    """Fetches conflict data from the ACLED API.

    Args:
        api_key (str): Your ACLED API key.
        email (str): Your ACLED account email.
        region (str, optional): The region to query. Defaults to "South Asia".
        start_date (str, optional): Start date in YYYY-MM-DD format. Defaults to None.
        end_date (str, optional): End date in YYYY-MM-DD format. Defaults to None.
        limit (int, optional): Number of records to fetch per request. Defaults to 1000.

    Returns:
        pd.DataFrame: A DataFrame containing the ACLED data.
    """
    base_url = "https://api.acleddata.com/acled/read"
    params = {
        "key": api_key,
        "email": email,
        "region": region,
        "limit": limit,
        "fields": "event_date|country|admin1|location|latitude|longitude|actor1|assoc_actor_1|interaction|event_type|fatalities"
    }

    if start_date:
        params["event_date"] = f">={start_date}"
    if end_date:
        params["event_date"] += f";<={end_date}" if start_date else f"<={end_date}"

    all_data = []
    page = 1
    while True:
        params["page"] = page
        try:
            response = requests.get(base_url, params=params)
            response.raise_for_status() # Raise an exception for HTTP errors
            data = response.json()

            if not data['data']:
                break # No more data

            all_data.extend(data['data'])

            # Check if there are more pages (ACLED API usually has a 'next_page_url' or similar in metadata)
            # For simplicity, we'll stop if a page returns less than the limit, assuming it's the last page.
            if len(data['data']) < limit:
                break
            page += 1

        except requests.exceptions.RequestException as e:
            print(f"Error fetching ACLED data: {e}")
            break

    return pd.DataFrame(all_data)

