import pandas as pd
import requests

def fetch_nasa_firms(api_key, bbox=[73.0, 27.0, 97.0, 37.0]):
    """Fetch Thermal Anomalies (Fires/Explosions) from NASA FIRMS.

    Args:
        api_key (str): Your NASA FIRMS Map Key.
        bbox (list): Bounding box [min_lon, min_lat, max_lon, max_lat].

    Returns:
        pd.DataFrame: DataFrame of FIRMS data, or empty DataFrame if error.
    """
    # Ensure bbox is a comma-separated string for the URL
    bbox_str = ",".join(map(str, bbox))
    url = f"https://firms.modaps.eosdis.nasa.gov/api/area/csv/{api_key}/VIIRS_SNPP_NRT/{bbox_str}/1"
    try:
        df = pd.read_csv(url)
        return df
    except requests.exceptions.RequestException as e:
        print(f"Error fetching NASA FIRMS data: {e}")
        return pd.DataFrame()
    except Exception as e:
        print(f"An unexpected error occurred: {e}")
        return pd.DataFrame()

